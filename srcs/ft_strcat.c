#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int n;
	int c;

	n = 0;
	c = 0;
	while (s1[n] != '\0')
		n++;
	while (s2[c] != '\0')
	{
		s1[n] = s2[c];
		c++;
		n++;
	}
	s1[n] = '\0';
	return (s1);
}
